    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        // TODO add your handling code here:
        if(addWordBar.getText().equals("") || addExplainBar.getText().equals(""))
            JOptionPane.showMessageDialog(this, "bạn cần nhập đầy đủ thông tin");
        else{
            Word w = new Word(addWordBar.getText(), addExplainBar.getText());
            int index = dictionaryApp.dictionary_Management.LookUp(addWordBar.getText());
            if(index >= 0) {
                JOptionPane.showMessageDialog(this, "từ vừa nhập đã có trong từ điển");
            }
            else {
                dictionaryApp.dictionary_Management.dictionary.getNewWord().add(w);
            }
            dictionaryApp.dictionary_Management.dictionarySort();            
            this.dispose();
            dictionaryApp.timer.start();
        }
    }//GEN-LAST:event_saveButtonActionPerformed

